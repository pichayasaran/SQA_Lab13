package main;

public class FizzBuzzApplication {
	 public String convert(int number){
		 if(number == 6){ 
	            return "Fizz";
	     }
		 if(number == 5){ 
	            return "Buzz";
	     }
		 if(number == 4){ 
	            return "4";
	     }
		 if(number == 3){ 
	            return "Fizz";
	     }
	     if(number == 2){ 
	            return "2";
	     }
	     return "1";
	 }
}

